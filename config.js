const path = require("path");

module.exports = {
  // Базовый адрес MTR API. Можно переопределить переменной окружения
  // MTR_BASE_URL, если адрес карты сервера изменится.
  BASE_URL: process.env.MTR_BASE_URL || "http://localhost:8888",

  // Измерение (0 = Overworld). У большинства серверов вся сеть в 0.
  DIMENSION: 0,

  // Как часто опрашивать топологию сети (станции/маршруты).
  // Она меняется редко, поэтому опрашиваем нечасто, чтобы не грузить сервер.
  TOPOLOGY_POLL_MS: 15 * 60 * 1000, // 15 минут — топология меняется редко

  // Как часто опрашивать отправления (фактическое расписание + опоздания).
  DEPARTURES_POLL_MS: 60 * 1000, // 60 секунд — снижает нагрузку на сервер

  // Порт локального веб-сервера.
  // PORT=0 заставляет ОС выбрать свободный порт (используется в Electron,
  // чтобы не конфликтовать с чужими процессами на 3000).
  PORT: process.env.PORT !== undefined ? Number(process.env.PORT) : 3000,

  // Где хранить данные (JSON-файлы, без внешней БД).
  // В Electron-сборке исходники лежат в read-only каталоге, поэтому путь
  // переопределяется переменной MTR_DATA_DIR (туда указывает userData).
  DATA_DIR: process.env.MTR_DATA_DIR || path.join(__dirname, "data"),

  // Сколько хранить историю опозданий (для аналитики), в миллисекундах.
  DELAY_HISTORY_RETENTION_MS: 14 * 24 * 60 * 60 * 1000, // 14 дней
};
